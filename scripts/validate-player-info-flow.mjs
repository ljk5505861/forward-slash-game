import assert from 'node:assert/strict';
import { readFileSync } from 'node:fs';
const game = readFileSync(new URL('../src/scenes/GameScene.js', import.meta.url), 'utf8');
const hud = readFileSync(new URL('../src/ui/Hud.js', import.meta.url), 'utf8');
const panel = readFileSync(new URL('../src/ui/PlayerInfoPanel.js', import.meta.url), 'utf8');
assert.match(game, /new PlayerInfoPanel\(this\)/);
assert.match(game, /createPlayerInfoButton\(\).*'角色'/s);
assert.match(game, /setDepth\(4250\)/, 'role button is above reward choice panels');
assert.match(game, /isRewardChoiceModalOpen\(\).*upgradePanel\?\.isOpen.*artifactRewardPanel\?\.isOpen.*professionPanel\?\.isOpen/s);
assert.match(game, /hasNonRewardBlockingModal\(\)/, 'non-reward blocking modals are checked explicitly');
assert.match(game, /hasBlockingModal\(\)&&!this\.isRewardChoiceModalOpen\(\)/, 'reward choices can stack player info but other blocking modals cannot');
assert.match(game, /playerInfoPanel\?\.update\(time\)/, 'scene drives player info realtime update');
assert.match(panel, /beginGameplayPause/);
assert.match(panel, /resumeModalFlow/);
assert.match(panel, /setInteractive\(\)/, 'mask intercepts input');
assert.match(panel, /pointerdown/); assert.match(panel, /pointerup/);
assert.match(panel, /REFRESH_INTERVAL_MS=100/);
assert.match(panel, /valueNodes=new Map/);
assert.match(panel, /this\.nodes=this\.nodes\.filter/, 'flipping removes destroyed page content from retained node list');
assert.match(panel, /_formatter=value=>value/, 'build page text is formatted without a leading label colon');
assert.match(panel, /width:full\?520:250/, 'attack row uses a full-width layout');
assert.match(panel, /refreshValues\(true\)/, 'render compatibility refreshes values');
assert.doesNotMatch(panel, /render\(\)\{ this\.content\.forEach/, 'render no longer destroys current page');
assert.match(panel, /`\$\{eff\}（基础\$\{base\}＋力量\$\{strength\}\）`/, 'attack split display uses full-width punctuation');
assert.match(panel, /当前状态|runState/);
assert.doesNotMatch(panel, /SKILLS/);
assert.doesNotMatch(panel, /playerData\.skills|p\.skills/);
assert.doesNotMatch(panel, /技能：/);
assert.doesNotMatch(hud, /技能：|职业：|法宝：|破军/);
console.log('player info modal validation passed');
