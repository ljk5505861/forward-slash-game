import assert from 'node:assert/strict';
import { readFileSync } from 'node:fs';
const combat=readFileSync(new URL('../src/systems/CombatSystem.js', import.meta.url),'utf8');
const game=readFileSync(new URL('../src/scenes/GameScene.js', import.meta.url),'utf8');
assert.match(combat, /flowBossType==='boss1'\)\{ s\.queueArtifactReward\?\.\(enemy,\{ afterBoss:'boss1' \}\)/, 'Boss 1 must queue artifact reward');
assert.match(combat, /flowBossType==='boss2'\) s\.startMidBossPostFightFlow\?\.\(\)/, 'Boss 2 must start profession flow');
assert.match(combat, /flowBossType==='boss3'\) s\.finishRun\?\.\(true\)/, 'Boss 3 must finish immediately');
assert.doesNotMatch(game, /startBossRewardFlow\(/, 'dedicated boss skill reward flow removed from GameScene');
assert.doesNotMatch(game, /rollBossRewardOptions\(/, 'GameScene must not roll boss skill rewards');
console.log('[validate:midboss-reward-flow] PASS boss rewards route to artifact/profession+artifact/victory flows');
